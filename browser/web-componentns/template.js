/*

    Шаблоны

    Если вам приходится постоянно применять одни и те же структуры в HTML-разметке веб-страниц, 
    полезно будет воспользоваться неким шаблоном вместо того, чтобы снова и снова писать один и тот же код. 
    Подобное было возможно и раньше, но теперь всё значительно упростилось благодаря появлению HTML-тега <template>, 
    который пользуется отличной поддержкой современных браузеров.


    Definition and Usage:

    The <template> tag holds its content hidden from the client.

    Content inside a <template> tag will not be rendered.

    The content can be visible and rendered later by using JavaScript.

    Use the <template> tag when you have HTML code you want to use over and over again,
    but not until you ask for it. To do this without the <template> tag, you have to create the HTML 
    code with JavaScript to prevent the browser from rendering the code.
    
*/